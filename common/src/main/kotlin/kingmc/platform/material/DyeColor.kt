package kingmc.platform.material

/**
 * An enum for colors for dyeable materials in minecraft, this could be applied to
 *  + Leather armors
 *  + Wools
 *  + Concrete (Powder)
 *  + Shulker Box
 *  + Stained Glass
 *  + (Glazed) Terracotta
 *  + Candle
 *
 * @since 0.0.6
 * @author kingsthere
 */
enum class DyeColor {
    /**
     * White
     */
    WHITE,
    /**
     * Orange
     */
    ORANGE,
    /**
     * Magenta
     */
    MAGENTA,
    /**
     * Light blue
     */
    LIGHT_BLUE,
    /**
     * Yellow
     */
    YELLOW,
    /**
     * Lime
     */
    LIME,
    /**
     * Pink
     */
    PINK,
    /**
     * Gray
     */
    GRAY,
    /**
     * Light Gray
     */
    LIGHT_GRAY,
    /**
     * Cyan
     */
    CYAN,
    /**
     * Purple
     */
    PURPLE,
    /**
     * Blue
     */
    BLUE,
    /**
     * Brown
     */
    BROWN,
    /**
     * Green
     */
    GREEN,
    /**
     * Red
     */
    RED,
    /**
     * Black
     */
    BLACK,
}